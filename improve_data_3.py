import pandas as pd
import re

# Загрузка данных
df = pd.read_csv('all_competencies_2.csv', encoding='utf-8')

# ----------------------------------------------------------------------
# 1. Функция для определения новой category_group
# ----------------------------------------------------------------------
def new_category_group(row):
    src = row['source']
    old_group = row['category_group']
    desc = row['description']
    code = row['code']
    
    # ----- ФГОС: ОПК (общие компетенции) -----
    if src == 'ФГОС' and code.startswith('ОПК'):
        # Разбиваем по ключевым словам в описании
        if 'руководить' in desc or 'толерантно' in desc or 'переговоры' in desc or 'ответственность' in desc:
            return 'Управление и коммуникации'
        if 'исследовать' in desc or 'научные' in desc or 'эксперименты' in desc or 'закономерности' in desc or 'публично' in desc:
            return 'Научные исследования и методология'
        if 'информационное общество' in desc or 'прикладная область' in desc:
            return 'Анализ и системное проектирование'
        if 'получения, хранения, переработки' in desc or 'компьютерные технологии' in desc:
            return 'Инфраструктура и DevOps'
        if 'анализировать профессиональную информацию' in desc or 'структурировать' in desc:
            return 'Анализ и системное проектирование'
        if 'иностранный язык' in desc or 'лексика' in desc:
            return 'Образование и саморазвитие'
        if 'саморегулированию' in desc or 'саморегулирование' in desc:
            return 'Образование и саморазвитие'
        if 'теоретические основы' in desc or 'категории' in desc:
            return 'Научные исследования и методология'
        if 'автоматизации проектирования' in desc or 'производства' in desc:
            return 'Архитектура и интеграция'
        if 'концептуальными положениями' in desc or 'программирования' in desc and 'направлений' in desc:
            return 'Разработка ПО'
        if 'систем реального времени' in desc:
            return 'Архитектура и интеграция'
        if 'системных программных средств' in desc:
            return 'Инфраструктура и DevOps'
        if 'моделирующих алгоритмов' in desc:
            return 'Научные исследования и методология'
        if 'моделей информационных технологий' in desc:
            return 'Анализ и системное проектирование'
        if 'выбора архитектуры' in desc or 'комплексирования' in desc:
            return 'Архитектура и интеграция'
        if 'выбора, проектирования, реализации' in desc or 'оценки качества' in desc:
            return 'Архитектура и интеграция'
        # Если ничего не подошло, оставляем старую группу (как запасной вариант)
        return old_group

    # ----- ФГОС: ПК (профессиональные) и другие -----
    if src == 'ФГОС':
        mapping = {
            'Программирование': 'Разработка ПО',
            'Проектирование / архитектура ПО': 'Архитектура и интеграция',
            'Управление ИТ-проектами': 'Управление проектами и процессами',
            'Техническая документация': 'Документация и стандарты',
            'Анализ данных': 'Данные и базы данных',
            'Математика и моделирование': 'Научные исследования и методология',
            'Администрирование / DevOps': 'Инфраструктура и DevOps',
            'Информационная безопасность': 'Безопасность',
            'Сети и ИКТ': 'Сети и телекоммуникации',
            'Прочее': 'Архитектура и интеграция'  # для 02.04.03 ОПК-6...11
        }
        return mapping.get(old_group, old_group)

    # ----- Профстандарты -----
    if src == 'Профстандарт':
        mapping = {
            'Программирование': 'Разработка ПО',
            'Проектирование / архитектура ПО': 'Архитектура и интеграция',
            'Работа с требованиями': 'Анализ и системное проектирование'
        }
        return mapping.get(old_group, old_group)

    # ----- Вакансии -----
    if src == 'Вакансия':
        mapping = {
            'Программирование': 'Разработка ПО',
            'Администрирование / DevOps': 'Инфраструктура и DevOps',
            'Анализ данных': 'Данные и базы данных',
            'Базы данных / SQL': 'Данные и базы данных',
            'Контейнеризация': 'Инфраструктура и DevOps',
            'Системы контроля версий': 'Разработка ПО',
            'Веб-разработка': 'Разработка ПО',
            'Прочее': 'Инфраструктура и DevOps'  # Azure
        }
        return mapping.get(old_group, old_group)

    # fallback
    return old_group

# ----------------------------------------------------------------------
# 2. Функция для обогащения category (для ФГОС)
# ----------------------------------------------------------------------
def new_category(row):
    src = row['source']
    code = row['code']
    desc = row['description']
    
    if src == 'ФГОС':
        # Берём первые несколько слов из описания, чтобы получить краткую суть
        # Убираем "способностью", "умением" и т.п.
        clean_desc = re.sub(r'^способностью |^умением |^готовностью |^владением |^знанием |^пониманием ', '', desc)
        # Ограничим длину 50 символов
        short = clean_desc[:50].strip()
        if short.endswith(','):
            short = short[:-1]
        return f"{code}: {short}"
    else:
        # Для профстандартов и вакансий оставляем исходное значение
        return code

# ----------------------------------------------------------------------
# Применяем преобразования
# ----------------------------------------------------------------------
df['category_group'] = df.apply(new_category_group, axis=1)
df['category'] = df.apply(new_category, axis=1)

# Сохраняем результат
df.to_csv('all_competencies_2_updated.csv', index=False, encoding='utf-8')

print("Обновлённый файл сохранён как 'all_competencies_2_updated.csv'")